function generateRandomString() {
    const lowercase = 'abcdefghijklmnopqrstuvwxyz';
    const uppercase = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ';
    const numbers = '0123456789';
    const allCharacters = lowercase + uppercase + numbers;
    
    let randomString = '';
    
   
    randomString += lowercase[Math.floor(Math.random() * lowercase.length)];
    randomString += uppercase[Math.floor(Math.random() * uppercase.length)];
    randomString += numbers[Math.floor(Math.random() * numbers.length)];
    randomString += numbers[Math.floor(Math.random() * numbers.length)];
    
    
    for (let i = randomString.length; i < 8; i++) {
        randomString += allCharacters[Math.floor(Math.random() * allCharacters.length)];
    }
    
    randomString = randomString.split('').sort(() => 0.5 - Math.random()).join('');
    
    return randomString;
}

function updateAttribute(array, attribute) {
    return array.map(obj => ({
        ...obj,
        [attribute]: generateRandomString()
    }));
}

